/*Procedimiento para buscar la donacion por medio del numero de recibo*/
-- VALIDAR EL NUMERO DE FACTURA 

CREATE OR REPLACE PROCEDURE Buscar_Donacion (pNumeroRecibo IN INT, pVERIFICAR OUT NUMBER)
AS
VERIFICAR NUMBER;
BEGIN
    SELECT COUNT(1)
    INTO VERIFICAR
    FROM 
    TBDonacion where ATNumeroRecibo = pNumeroRecibo;
    IF VERIFICAR = 1 THEN
        pVERIFICAR := 1;
    ELSE
        pVERIFICAR := 0;
    END IF;
END;
